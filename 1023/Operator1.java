
public class Operator1 {

	public static void main(String[] args) {
		//정수형 변수 2개 선언
		int num1=10,num2=7;
		int result = num1 + num2;
		System.out.println(result);
		result = num1 - num2;
		System.out.println(result);
		result = num1 * num2;
		System.out.println(result);
		result = num1 / num2;
		System.out.println(result);
		result = num1 % num2;
		System.out.println(result);
	}

}

